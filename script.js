const mantraButton = document.getElementById("mantra-button");
const mantraOutput = document.getElementById("mantra-output");
const modeSelect = document.getElementById("mode-select");
const modeResult = document.getElementById("mode-result");
const langButtons = document.querySelectorAll(".lang-button");
const translatableNodes = document.querySelectorAll("[data-i18n]");
const portraitStage = document.getElementById("portrait-stage");

let currentLang = "en";

function updateTranslations(lang) {
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";

  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (copy[lang][key]) {
      node.textContent = copy[lang][key];
    }
  });

  mantraOutput.textContent = mantras[lang][0];
  modeResult.textContent = modeCopy[lang][modeSelect.value];

  langButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === lang);
  });
}

mantraButton.addEventListener("click", () => {
  const options = mantras[currentLang];
  const next = options[Math.floor(Math.random() * options.length)];
  mantraOutput.textContent = next;
});

modeSelect.addEventListener("change", (event) => {
  modeResult.textContent = modeCopy[currentLang][event.target.value];
});

langButtons.forEach((button) => {
  button.addEventListener("click", () => {
    currentLang = button.dataset.lang;
    updateTranslations(currentLang);
  });
});

